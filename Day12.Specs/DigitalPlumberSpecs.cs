        [Test]
        public void when_parsing_pipes_and_the_pipes_are_empty()
        {
            var pipesPrimitve = "";
            var pipes = ClassUnderTest.ParsePipes(pipesPrimitve);
            Assert.AreEqual(new Dictionary<int, int[]>(), pipes);
        }

        [Test]
        public void when_parsing_pipes_and_the_pipes_have_one_pipe()
        {
            var pipesPrimitve = "0 <-> 2";
            var pipes = ClassUnderTest.ParsePipes(pipesPrimitve);
            Assert.AreEqual(new Dictionary<int, int[]> { { 0, new[] { 2 } } }, pipes);
        }

        [Test]
        public void when_parsing_pipes_and_the_pipes_have_three_pipes()
        {
            var pipesPrimitve = "0 <-> 2\n1 <-> 1\n2 <-> 0,3,4";
            var pipes = ClassUnderTest.ParsePipes(pipesPrimitve);
            Assert.AreEqual(new Dictionary<int, int[]>
            {
                { 0, new[] { 2 } },
                { 1, new []{ 1 } },
                { 2, new []{ 0, 3, 4 } }
            }, pipes);
        }

        [Test]
        public void when_counting_connected_programs_example()
        {
            var pipes = "0 <-> 2\n1 <-> 1\n2 <-> 0,3,4\n3 <-> 2,4\n4 <-> 2,3,6\n5 <-> 6\n6 <-> 4,5";
            var count = ClassUnderTest.PipesConnectedToProgram(pipes);
            Assert.AreEqual(6, count);
        }

        [Test]
        public void when_counting_program_groups_example()
        {
            var pipes = "0 <-> 2\n1 <-> 1\n2 <-> 0,3,4\n3 <-> 2,4\n4 <-> 2,3,6\n5 <-> 6\n6 <-> 4,5";
            var count = ClassUnderTest.CountProgramGroups(pipes);
            Assert.AreEqual(2, count);
